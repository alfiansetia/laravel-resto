    public function index(Request $request)
    {
        if ($request->ajax()) {
            $data = Cart::with('user', 'menu')->get();
            if ($request->name) {
                $data = Cart::where('name', 'like', "%{$request->name}%")->get();
            }
            return DataTables::of($data)->toJson();
        }
        return view('cart.data')->with(['comp' => $this->comp, 'title' => 'New Order']);
    }

